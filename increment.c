// Post increment operator

#include<stdio.h>
int main(){
    int i=1;
    
    printf("%d\n", i++); // use, then increase
    printf("%d", i);
    return 0;
}

// Pre increment operator

#include<stdio.h>
int main(){
    int i=1;
    
    printf("%d\n", ++i); // increase, then use
    printf("%d", i);
    return 0;
}
